          <h1 class="document-first">Causas e principais sintomas </h1>
          <p class="card-texto">
          A depressão é considerada pela Organização Mundial da Saúde (OMS) como o "Mal do Século". No sentido patológico, há presença de tristeza, pessimismo, baixa auto-estima, que aparecem com freqüência e podem combinar-se entre si. A depressão provoca ainda ausência de prazer em coisas que antes faziam bem e grande oscilação de humor e pensamentos, que podem culminar em comportamentos e atos suicidas. 
         </p>
         <p class="texto"> O tratamento é feito com auxílio médico profissional, por meio de medicamentos, e acompanhamento terapêutico conforme cada caso. O apoio da família é fundamental. <p>
         <p class="texto">Está presente na literatura médica e científica mundial que a depressão também incita alterações fisiológicas no corpo, sendo porta de entrada para outras doenças. Pessoas acometidas por depressão podem, além da sensação de infelicidade crônica e prostração, apresentar baixas no sistema de imunidade e maiores episódios de problemas inflamatórios e infecciosos. A depressão, dependendo da gravidade, pode desencadear, também, doenças cardiodasculares, como enfarto, AVC e hipertensão.</p>
         <p class="texto ">Há uma série de evidências que mostram alterações químicas no cérebro do indivíduo deprimido, principalmente com relação aos neurotransmissores (serotonina, noradrenalina e, em menor proporção, dopamina), substâncias que transmitem impulsos nervosos entre as células. Ao contrário do que normalmente se pensa, os fatores psicológicos e sociais muitas vezes são conseqüência e não causa da depressão. Vale ressaltar que o estresse pode precipitar a depressão em pessoas com predisposição, que provavelmente é genética.</p>
         <p class="texto ">Estima-se que uma em cada cinco pessoas no mundo apresentam problemas relacionados a depressão em algum momento da vida. A melhor forma de prevenir a depressão é cuidando da mente e do corpo, com alimentação saudável e prática de atividades físicas regulares. Saber lidar com o estresse e compartilhar os problemas com amigos ou familiares é outra alternativa, que pode ser aliada à prática de alguma atividade integrativa e complementar, como yoga, por exemplo. Ajudam a prevenir a depressão leitura, aprender coisas novas, ter hobbies, viajar e se divertir. Essas práticas mantém a cabeça ativa e a ocupam com pensamentos positivos. A ciência já comprovou que cuidar do corpo reflete na saúde mental de forma positiva. Atividades físicas liberam hormônios e outras substâncias importantes para manutenção do humor. Na alimentação, receitas ou dietas recheadas de azeite de oliva, peixes, frutas, verduras e oleaginosas (nozes, castanhas etc) são o ideal para prevenir depressão. Esses produtos são ricos em nutrientes que protegem e conversam a rede de neurônios.</p>
         <p class="texto ">Além das alterações de humor ou irritabilidade, ansiedade e angústia a depressão possui diversos sinais e sintomas, que podem ser isolados ou somatizados. Os principais sintomas da depressão são:</p>
         <ul>
          <li>irritabilidade, ansiedade e angustia;</li>
          <li>desânimo, cansaço fácil, necessidade de maio esforço para fazer as coisas;</li>
          <li>diminuição ou incapacidade de sentir alegria e prazer;</li>
          <li>desinteresse, falta de motivação e apatia</li>
          <li>pessimismo, ideias frequentes e desproporcionais de culpa, baixa auto-estima;</li>
          <li>sensação, inutilidade, ruína e fracasso;</li>
          <li>interpretação distorcida e negativa da realidade;</li>
          <li>dificuldade de concentração, raciocínio mais lento e esquecimento;</li>
          <li>diminuição do desempenho sexual;</li>
          <li>perda ou aumento do apetite e do peso;</li>
          <li>insônia ou despertar matinal precoce;</li>
          <li>dores e outros sintomas físicos não justificados por problemas médicos; </li>
          <li>sentimentos de medo, insegurança, desesperança, desespero e desamparo;</li>
         </ul>
         <p>Existem alguns fatores de risco que podem contribuir para o desenvolvimento da depressão. </p>
         <ul>
          <li>Histórico familiar</li>
          <li>Transtornos psiquiátricos correlatos</li>
          <li>Estresse crônico</li>
          <li>Ansiedade crônica</li>
          <li>Disfunções hormonais</li>
          <li>Excesso de peso</li>
          <li>Sedentarismo e dieta desregrada</li>
          <li>Vícios (cigarro, álcool e drogas ilícitas)</li>
          <li>Uso excessivo de internet e redes sociais</li>
          <li>Traumas físicos ou psicológicos</li>
          <li>Pancadas na cabeça</li>
          <li>Problemas cardíacos</li>
          <li>Separação conjugal</li>
          <li>Enxaqueca crônica</li>
         </ul>
         <p>Como diferenciar tristeza e depressão?</p>
         
         <ul> 
          <li>Tristeza tem motivo. A pessoa sabe que está triste.</li>
          <li>A depressão é uma tristeza profunda e muitas vezes sem conteúdo, sem motivo aparente. Mesmo se algo maravilhoso acontecer ou estiver acontecendo, a pessoa continuará triste.</li>
          <li>A pessoa triste pode ter sintomas no corpo, como sentir aperto no perito, taquicardia, chorar. </li>
          <li>A pessoa deprimida tem pensamentos suicidas. </li>
          <li>Quem está triste costuma ter pensamentos repetitivos sobre a razão da tristeza.</li>
          <li>Quando deprimida, a pessoa sente, pelo menos, duas semanas de uma tristeza profunda e contínua.</li>
        </ul>
        <p>Como é feito o diagnóstico da depressão?</p>
        <br>
        <p>O diagnóstico da depressão é clínico e somente pode ser dado por um médico especialista, no caso o psiquiatra, que é responsável por tratar pessoas com transtornos mentais.  </p>
        <br>
        <p>Como saber se a pessoa sofre de depressão?</p>
        <br>
        <p>Durante uma consulta com um médico especialista serão feitos alguns testes e questionários, que podem apontar para o distúrbio. </p>
        <br>
        <p>Nesse momento, o psiquiatra fará, também, outras observações, como histórico do paciente e familiares, e poderá pedir alguns exames laboratoriais específicos para se chegar ao diagnóstico.</p>
         <br>
        <p>A depressão também pode estar associada a outros transtornos psiquiátricos e tem níveis de intensidade. Pode ser leve, moderada ou grave.</p>
         <br>
        <p>Cada caso é avaliado individualmente e cada paciente recebe um diagnóstico e é encaminhado para tratamento específico.</p>
         <br>
        <p>Tratamento :</p>
         <br>
        <p>O tratamento da depressão é essencialmente medicamentoso. Existem mais de 30 antidepressivos disponíveis.</p>
         <br>
        <p>Ao contrário do que alguns temem, essas medicações não são como drogas, que deixam a pessoa eufórica e provocam vício. </p>
         <br>
        <p>A terapia é simples e, de modo geral, não incapacita ou entorpece o paciente. </p>
         <br>
        <p>Alguns pacientes precisam de tratamento de manutenção ou preventivo, que pode levar anos ou a vida inteira, para evitar o aparecimento de novos episódios.</p>
         <br>
        <p>A psicoterapia auxilia na reestruturação psicológica do indivíduo, além de aumentar sua compreensão sobre o processo de depressão e na resolução de conflitos, o que diminui o impacto provocado pelo estresse.</p>
         <br>
        <p>A depressão não tem tempo para passar. Pode durar dias, semanas, meses ou anos.</p>
         <br>
        <p>A pessoa em crise, após superar o transtorno mental, também pode, a qualquer momento, experimentar novos episódios da depressão. </p>
        <br>
        <p>Na maioria das vezes, o tratamento para depressão é feito combinando psiquiatra e psicoterapia, por meio de psicólogos.</p>
         <br>
        <p>Existem também medicamentos antidepressivos, que ajudam a regular a química cerebral e é aplicado conforme cada caso, de acordo com cada paciente.</p>
