         <p class="texto">Outras crianças podem tornar-se agitadas e/ou agressivas, visto que todas as crianças necessitam de atenção, amor e limites. Uma mãe deprimida pode não conseguir dar isto na medida certa e esta falta, muitas vezes, não é suprida por outro adulto responsável. Então, torna-se um hábito ter atitudes negativas na busca por chamar a atenção, as quais podem ir se agravando com o passar dos anos, atingindo seus relacionamentos e conquistas por toda a vida.</p>
