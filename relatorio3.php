         <p class="texto">Estas crianças tendem a sentir-se perdidas sem a orientação de uma mãe confiante a preparada para os desafios do dia a dia. Portanto, poderão desenvolver uma personalidade frágil, incapaz de lidar com as vicissitudes da vida ou poderão sentir-se responsáveis pelo bem estar da mãe, deixando de viver plenamente sua infância, o que fará falta em algum momento da vida.</p>
         <br/>
